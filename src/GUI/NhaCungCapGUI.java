    private void jButton_ThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_ThemActionPerformed
        // TODO add your handling code here:
        // Lấy dữ liệu từ jTextField
        String ten = jTextField_Ten.getText();
        String sdt = jTextField_SDT.getText();
        String diachi = jTextField_DiaChi.getText();
        String loaisanpham = jCheckBox_OPPO.toString();
        
        
        try{
            // Kiểm tra nếu cả hai trường không rỗng
            if (!ten.isEmpty() && !loaisanpham.isEmpty() && !sdt.isEmpty() && !diachi.isEmpty())
            JOptionPane.showMessageDialog(this, "Vui lòng nhập đủ thông tin");
            else{
                NhaCungCapDTO ncc=new NhaCungCapDTO();
                if(arrNhaCungCap.size()==0){
                    ncc.setMaNCC("NCC001");
                }
                else {
                    NhaCungCapDTO NV = arrNhaCungCap.get(arrNhaCungCap.size()-1);
                    int id = Integer.parseInt(NV.getMaNCC().substring(3));

                    if(id<=9)
                    ncc.setMaNCC("NCC00"+(id+1));
                    else if(id>=10 && id<=99)
                    ncc.setMaNCC("NCC0"+(id+1));
                    else
                    ncc.setMaNCC("NCC"+(id+1));
                }
                ncc.setTenNCC(jTextField_Ten.getText());
                ncc.setDiaChiNCC(jTextField_DiaChi.getText());
                ncc.setSDTNCC(jTextField_SDT.getText());

                nccBLL.addNCC(ncc);
                loadNCClist();
            }

        }
        catch (Exception ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this, "Thông tin không hợp lệ");
        }
        // Thêm dữ liệu mới vào mô hình

    }//GEN-LAST:event_jButton_ThemActionPerformed

